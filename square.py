def area(a):
    '''
    Возваращает площадь квадрата по стороне.

        Парамаетры:
            a (int): сторона квадрата

        Возвращаемое значение:
            area (int): площадь квадрата
    '''
    return a ** 2


def perimeter(a):
    '''
    Возвращает периметр квадрата по стороне.

        Параметры:
            a (int): сторона квадрата

        Возвращаемое значения:
            perimeter (int): периметр квадрата
    '''
    return a * 4
